package ash.advanced.java;

//to check your pc compliance level java 7 plus 
// java7 has the feature of switching Strings 
// if this program runs then you are on the perfect track or else you may have to configure your environment
public class Java7plus {

	public static void main(String[] args) {
		String key = "One";
		switch (key) {
		case "One":
			System.out.println("The value is one");
			break;

		default:

			System.out.println("The value is not one");
			break;
		}
	}

}
